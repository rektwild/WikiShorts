def update_localizable_strings(file_path, lang_code):
    """Update a Localizable.strings file with missing translations."""
    if not os.path.exists(file_path):
        return False
    
    try:
        with open(file_path, 'r', encoding='utf-8') as f:
            content = f.read()
    except:
        return False
    
    modified = False
    
    for key, translations in ONBOARDING_TRANSLATIONS.items():
        # Get the translation for this language, or fall back to English
        if lang_code in translations:
            translation = translations[lang_code]
        elif lang_code == "en":
            translation = translations.get("en", "")
        else:
            # Skip if no translation available for this language
            continue
        
        # Don't update if translation contains "Delays" (placeholder)
        if "Delays" in translation or "delays" in translation:
            continue
        
        # Check if key exists with English placeholder
        pattern = f'"{key}" = "([^"]+)";'
        match = re.search(pattern, content)
        
        if match:
            current_value = match.group(1)
            # Check if current value is English (needs translation)
            english_value = translations.get("en", "")
            if current_value == english_value and lang_code != "en":
                # Replace with proper translation
                old_line = f'"{key}" = "{current_value}";'
                new_line = f'"{key}" = "{translation}";'
                content = content.replace(old_line, new_line)
                modified = True
                print(f"  Updated {key}: {current_value} -> {translation}")
    
    if modified:
        with open(file_path, 'w', encoding='utf-8') as f:
            f.write(content)
    
    return modified


