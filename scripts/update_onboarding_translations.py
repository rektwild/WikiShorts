    base_path = "/Users/sefacemturan/WikiShorts/WikiFlick"
    
    # Find all lproj directories
    for item in os.listdir(base_path):
        if item.endswith('.lproj'):
            lang_code = item.replace('.lproj', '')
            file_path = os.path.join(base_path, item, 'Localizable.strings')
            
            if os.path.exists(file_path):
                print(f"\nProcessing {lang_code}...")
                update_localizable_strings(file_path, lang_code)
    
    print("\n✅ Onboarding translations updated!")


